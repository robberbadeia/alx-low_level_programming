#include <stdlib.h>
#include <time.h>
#include <stdio.h>

/**
 * main - Determines if a number is positive, negative or zero.
 *
 * Return: Always 0 (Success)
 */
int main(void)
{
	char lAlpha = 'a';
	char uAlpha = 'A';

	while (lAlpha <= 'z')
	{
		putchar(lAlpha);

		lAlpha++;
	}

	while (uAlpha <= 'Z')
	{
		putchar(uAlpha);

		uAlpha++;
	}

	putchar('\n');
	return (0);





	return (0);
}
